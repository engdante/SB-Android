"""
Audio transcription postprocessor module.
Пост-обработка на транскрибирания текст:
1. Regex за слепване на разкъсани думи (quick fix)
2. LLM пост-процесинг за цялостно подобряване (същност, пунктуация)

Проблемът с whisper.cpp е, че понякога къса думите:
  "инструмент ите" -> "инструментите"
  "веч еря" -> "вечеря"
"""

import re
from typing import Optional
from loguru import logger


# ──────────────────────────────────────────────
# Regex пост-процесор (без LLM, много бърз)
# ──────────────────────────────────────────────

def _fix_split_words(text: str) -> str:
    """
    Слепва разкъсани думи.

    Детектира случаи като:
    - "инструмент ите" → "инструментите"
    - "веч еря" → "вечеря"
    - "по дреди" → "подреди"

    Логика:
    - Ако дума завършва на съгласна и следващата дума започва с гласна
      или същата съгласна, и двете заедно образуват валидна българска дума
      (повече от 2 букви) -> слепва ги.
    """
    # Шаблон: дума от 2+ букви, пауза (space/newline), после 2+ букви
    # Слепване когато комбинацията е смислена
    text = text.replace("\n", " ")

    # Патерн: съгласна + space + гласна/съгласна (но не цели думи)
    # Ще слепваме когато:
    # 1. Първата част завършва на съгласна и е кратка (2-5 букви)
    # 2. Втората част започва с гласна или съгласна и е кратка (2-5 букви)
    # 3. Общата дължина е > 3 букви

    # Първо: специфични често срещани разкъсвания
    known_splits = {
        r"\bинструмент\s+ите\b": "инструментите",
        r"\bинструмент\s+и\b": "инструменти",
        r"\bвеч\s+еря\b": "вечеря",
        r"\bвеч\s+ер\b": "вечер",
        r"\bпо\s+дреди\b": "подреди",
        r"\bпо\s+дред\b": "подред",
        r"\bпо\s+дна\b": "подна",
        r"\bпо\s+лагам\b": "поставям",
        r"\bпо\s+ложени\b": "поставени",
        r"\bиз\s+чистя\b": "изчистя",
        r"\bиз\s+мия\b": "измия",
        r"\bиз\s+ползваш\b": "използваш",
        r"\bиз\s+ползва\b": "използва",
        r"\bпри\s+бера\b": "прибера",
        r"\bпри\s+бери\b": "прибери",
        r"\bна\s+прави\b": "направи",
        r"\bна\s+правя\b": "направя",
        r"\bдо\s+бре\b": "добре",
        r"\bдо\s+бра\b": "добра",
        r"\bза\s+щото\b": "защото",
        r"\bза\s+що\b": "защо",
        r"\bка\s+кво\b": "какво",
        r"\bка\s+кто\b": "както",
        r"\bмо\s+же\b": "може",
        r"\bмо\s+га\b": "мога",
        r"\bтряб\s+ва\b": "трябва",
        r"\bи\s+мат\b": "имат",
        r"\bи\s+ма\b": "има",
        r"\bсъ\s+м\b": "съм",
        r"\bсъ\s+м\b": "съм",
        r"\bще\s+м\b": "щем",
        r"\bще\s+те\b": "щете",
        r"\bпо\s+требно\b": "потребно",
        r"\bра\s+бота\b": "работа",
        r"\bра\s+боти\b": "работи",
        r"\bста\s+я\b": "стая",
        r"\bста\s+и\b": "стаи",
        r"\bдва\s+мата\b": "двамата",
        r"\bсед\s+нем\b": "седнем",
    }

    for pattern, replacement in known_splits.items():
        text = re.sub(pattern, replacement, text)

    # Генеричен патерн за слепване на split-нати думи:
    # Ако дума от 2-6 букви завършва на съгласна и следващата дума от 2-4 букви
    # започва с гласна -> слепва ги
    # (но само ако комбинацията е > 3 букви)
    generic_pattern = re.compile(
        r'\b([а-яА-Я]{2,6}[бвгджзклмнпрстфхцчшщ])\s+([аеиоуъяю]{2,4})\b',
        re.UNICODE
    )

    def _merge_generic(m: re.Match) -> str:
        part1 = m.group(1)
        part2 = m.group(2)
        merged = part1 + part2
        if len(merged) > 3:
            return merged
        return m.group(0)

    text = generic_pattern.sub(_merge_generic, text)

    return text


def _fix_punctuation(text: str) -> str:
    """
    Подобрява пунктуацията:
    - Слага точка в края ако липсва
    - Слага запетая след "което", "който", "където", "че", "защото" и др.
    - Прави първата буква главна
    """
    text = text.strip()

    if not text:
        return text

    # Прави първата буква главна
    text = text[0].upper() + text[1:]

    # Слага точка в края ако няма
    if text[-1] not in ".!?":
        text += "."

    # Разделя на изречения по ". " и прави всяко с главна буква
    sentences = re.split(r'(?<=[.!?])\s+', text)
    sentences = [s[0].upper() + s[1:] if s and len(s) > 1 else s for s in sentences if s]
    text = " ".join(sentences)

    return text


def _remove_repeated_spaces(text: str) -> str:
    """Премахва повтарящи се интервали и white-space."""
    text = re.sub(r'\s+', ' ', text)
    return text.strip()


def regex_postprocess(text: str) -> str:
    """
    Бърз regex пост-процесинг.
    """
    if not text or not text.strip():
        return text

    text = _remove_repeated_spaces(text)
    text = _fix_split_words(text)
    text = _fix_punctuation(text)

    return text


# ──────────────────────────────────────────────
# LLM пост-процесор (по-бавен, но много по-добър)
# ──────────────────────────────────────────────

# System prompt за LLM пост-процесинга
_POSTPROCESS_SYSTEM_PROMPT = """Ти си асистент за пост-обработка на транскрибирана реч.

Твоята задача е да:
1. Слепиш разкъсаните думи (напр. "инструмент ите" → "инструментите", "веч еря" → "вечеря")
2. Подобриш пунктуацията (точки, запетаи, главни букви)
3. Разделиш текста на изречения
4. Премахнеш повторенията и поправиш малки грешки от транскрипцията

ВАЖНО:
- НЕ добавяй нов смисъл или информация
- НЕ пренаписвай съдържанието
- НЕ променяй фактологическата информация
- Запази стила на говорене (първо лице, разговорен тон)
- Само подобри форматирането и слепи разкъсаните думи
- Върни само коригирания текст, без обяснения"""


async def llm_postprocess(text: str, llm_client) -> str:
    """
    LLM пост-процесинг на транскрибиран текст.
    Използва Ollama клиент за корекция на разкъсани думи и пунктуация.

    Args:
        text: Транскрибираният текст
        llm_client: OllamaClient инстанция за изпращане на заявки

    Returns:
        Коригираният текст
    """
    if not text or not text.strip():
        return text

    # Първо бърз regex fix (за спестяване на LLM токени)
    text = regex_postprocess(text)

    # Ако текстът е много кратък, няма нужда от LLM
    if len(text) < 20:
        return text

    logger.info(f"LLM пост-процесинг: {len(text)} символа")

    # Prompt за LLM
    prompt = (
        "Коригирай този български транскрибиран текст:\n\n"
        "Правила:\n"
        "1. Слепи разкъсаните думи (напр. \"инструмент ите\" → \"инструментите\")\n"
        "2. Подобри пунктуацията (точки, запетаи, главни букви)\n"
        "3. Раздели на изречения\n"
        "4. Премахни повторенията\n"
        "5. НЕ добавяй нов смисъл\n"
        "6. НЕ променяй съдържанието\n\n"
        "Текст за корекция:\n"
        "---\n"
        f"{text}\n"
        "---\n\n"
        "Върни само коригирания текст:"
    )

    try:
        corrected = await llm_client.generate(
            prompt=prompt,
            system_prompt=_POSTPROCESS_SYSTEM_PROMPT,
            temperature=0.1,  # много ниска температура - искаме точност, не креативност
            max_tokens=len(text) * 2  # достатъчно токени за коригирания текст
        )

        if corrected:
            # Изчистваме евентуални кавички около текста
            corrected = corrected.strip().strip('"').strip("'").strip('"')
            logger.info(f"LLM пост-процесинг: {len(text)} -> {len(corrected)} символа")
            return corrected

        # Ако LLM върне празен текст, връщаме regex-обработения
        logger.warning("LLM пост-процесинг върна празен текст, използвам regex резултата")
        return text

    except Exception as e:
        logger.error(f"LLM пост-процесинг грешка: {e}, използвам regex резултата")
        return text


# ──────────────────────────────────────────────
# Комбиниран пост-процесор
# ──────────────────────────────────────────────

async def full_postprocess(text: str, llm_client=None, use_llm: bool = True) -> str:
    """
    Пълен пост-процесинг на транскрибиран текст.

    Args:
        text: Транскрибираният текст
        llm_client: OllamaClient (задължителен ако use_llm=True)
        use_llm: Дали да използва LLM пост-процесинг

    Returns:
        Коригираният текст
    """
    # Винаги първо regex (бързо)
    result = regex_postprocess(text)

    if use_llm and llm_client:
        result = await llm_postprocess(result, llm_client)

    return result